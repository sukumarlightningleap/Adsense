/**
 * Tiny placeholder PNG used for demo asset bytes.
 *
 * The Python seeder generates labeled placeholder PNGs via Pillow. We
 * don't need real images for demo data (they never get uploaded to
 * Google Ads — demo campaigns stay in the local DB), so we store this
 * minimal 8×8 gray PNG instead and let the asset's `name` carry the
 * human-readable label.
 *
 * Saves a ~20MB native `sharp` dependency and zero IO at seed time.
 */
const PLACEHOLDER_PNG = new Uint8Array([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, 0x00, 0x00, 0x00, 0x0d,
  0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x08, 0x00, 0x00, 0x00, 0x08,
  0x08, 0x02, 0x00, 0x00, 0x00, 0x4b, 0x6d, 0x29, 0xdc, 0x00, 0x00, 0x00,
  0x0f, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9c, 0x63, 0xc0, 0xc0, 0xc0, 0x80,
  0x00, 0x00, 0x00, 0x04, 0x00, 0x01, 0x27, 0x10, 0x5c, 0x12, 0x00, 0x00,
  0x00, 0x00, 0x49, 0x45, 0x4e, 0x44, 0xae, 0x42, 0x60, 0x82,
]);

export function placeholderPng(): Uint8Array {
  // Return a copy so callers can't mutate the shared array.
  return new Uint8Array(PLACEHOLDER_PNG);
}

